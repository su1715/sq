#include "bsq_lib.h"

int	put_each(t_map *map, int x, int y, char c)
{
	if (c == map->empty)
		map->grid[x][y] = 1;
	else if (c == map->obstacle)
		map->grid[x][y] = 0;
	else
		return (0);
	return (1);
}
